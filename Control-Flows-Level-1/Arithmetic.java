import java.util.Scanner;
class Arithmetic{
	public static void main(String args[]){
		Scanner sc=new Scanner (System.in);
		int a=sc.nextInt();
		int b=sc.nextInt();
		System.out.println("enter a+b" +(a+b) );
		System.out.println("enter a-b" +(a-b) );
		System.out.println("enter a/b" +(a/b) );
		System.out.println("enter a*b" +(a*b) );
	
}
}